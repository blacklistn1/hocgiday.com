                <p>Tương tự với những hợp khác, bạn chỉ việc đọc từ trái sang phải là OK
                                Riêng với hợp âm Dominant, thường thì ký hiệu sẽ không có chữ Dominant. Thường các bạn sẽ thấy là G7 (thay vì Gdom7)</p>
                <h2>Thế nào là một hợp âm cơ bản</h2>
               <p> Hợp âm cơ bản là hợp âm được sử dụng rất phổ biến trong các bài nhạc. Nó giống như màu sắc và những âm sắc cơ bản nhất vậy. Những bài hát phổ biến hiện nay hầu hết xây dựng dựa trên những bộ hoặc vòng hòa thanh cơ bản như: cơn mưa ngang qua, em của ngày hôm qua, ngày mai em đi, bay, love yourself,… Hợp âm cơ bản thường được xây dựng trên 3 bậc cơ bản: đó là I, III, V. Khi bạn sử dụng hợp âm có bậc VII, có thể đó gọi là hợp âm cơ bản được rồi, vì khi nghe màu 7, cảm xúc của bạn sẽ rất khác.</p>
               <img src="<?php echo imgsrc('')?>baiviet/24hopam/3-bac-hop-am.jpg" alt="3-bac-hop-am" class="limited">
               <p>Ví dụ: màu Cmaj7 sẽ thanh thanh và nhẹ nhàng hơn màu của Cmaj. </p>
                <p>Nhưng hầu hết những hợp âm nâng cao này được xây dựng từ những hợp âm cơ bản. Nên chỉ cần bạn biết được hết những hợp âm cơ bản là bạn đã có thể tự suy ra những hợp âm khác bằng cách thêm các nốt khác vào là ok.</p>

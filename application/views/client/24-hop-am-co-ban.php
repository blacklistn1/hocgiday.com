                <h2>Khái niệm về hợp âm?</h2>
                
                <p>Hợp âm được hiểu đơn giản là tập hợp những nốt nhạc được xếp lại có sẵn. Sau đó được người chơi đàn đánh cùng lúc để tạo nên một âm thanh phù hợp với sắc thái. Có nhiều cách hiểu khác nhau về hợp âm, nhưng tựu trung lại đối với người mới bắt đầu thì chúng ta chỉ cần hiểu đó là một màu sắc âm thanh, mà giới khoa học âm nhạc đã nghiên cứu và chỉ ra trước đó. Nhiệm vụ của ta chỉ là học thuộc và chơi lại, tùy thuộc từng bài hát mà chơi cho phù hợp.</p>
                <h2>Cấu tạo một hợp âm?</h2>
                <p>Trong âm nhạc nói chung, ta có 7 nốt nhạc chính đó là do – re – mi – pha – son –la –si, mà chúng ta thường gọi tắt đó là C – D – E –F –G –A –B . Những hợp âm sẽ được xây dựng chủ yếu dựa trên 7 nốt nhạc này, kèm theo đó là những kí hiệu mà bạn có thể thường thấy như:</p>
                <ul><li>Maj = Major = Trưởng</li>
                    <li>Min = Minor = Thứ</li>
                    <li>Dominant = hợp âm át</li>
                    <li># : dấu thắng</li>
                    <li>b : dấu giáng</li>
                    <li>7 : hợp âm 7</li>
                </ul>
                <h3>Ví dụ:</h3> 
                <ul>
                    <li>Cmaj7? – Đọc là Đô trưởng 7 – Sở dĩ tại sao lại gọi như vậy, vì hợp âm này được xây dựng dựa trên 3 nốt cơ bản là Đô – Mi – Son và nốt bậc 7 ( trong trường hợp này là Si)</li>
                    <li>Am7? – Đọc là La thứ 7</li>
